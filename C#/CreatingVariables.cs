using System;
class Programa
{
    static void Main(string[] args)
    {
        Console.WriteLine("Projeto 2 - Criando Variáveis");

        int idade;

        idade = 56;

        Console.WriteLine("Minha idade é " + idade);

        idade = 56 - 5;

        Console.WriteLine(idade);

        idade = 8 * 2 - 6;

        Console.WriteLine(idade);

        idade = (7 - 2) * 3;

        Console.WriteLine(idade);

        Console.WriteLine("Tecle enter para finalizar o programa.");
        Console.ReadLine();

    }
}
